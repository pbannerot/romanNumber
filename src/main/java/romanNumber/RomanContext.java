package romanNumber;

/**
 * @author pascal
 *
 */
public class RomanContext {
	private String input;
	private int output;

	public RomanContext(String input) {
		this.input = input;
	}

	public String getInput() {
		return input;
	}

	public void setInput(String input) {
		this.input = input;
	}

	public int getOutput() {
		return output;
	}

	public void setOutput(int output) {
		this.output = output;
	}
}
